package org.goskyer.report.bean;

public class BambooBean {

    private int id;
    private String radical;
    private String sound;
    private String rhyme;
    private String initial;
    private String word;
    private String syncopated_figure;
    private String bamboo_figure;
    private String grapheme;
    private String source;
    private String text_name;
    private String remarks;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getRadical() {
        return radical;
    }

    public void setRadical(String radical) {
        this.radical = radical;
    }

    public String getSound() {
        return sound;
    }

    public void setSound(String sound) {
        this.sound = sound;
    }

    public String getRhyme() {
        return rhyme;
    }

    public void setRhyme(String rhyme) {
        this.rhyme = rhyme;
    }

    public String getInitial() {
        return initial;
    }

    public void setInitial(String initial) {
        this.initial = initial;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public String getSyncopated_figure() {
        return syncopated_figure;
    }

    public void setSyncopated_figure(String syncopated_figure) {
        this.syncopated_figure = syncopated_figure;
    }

    public String getBamboo_figure() {
        return bamboo_figure;
    }

    public void setBamboo_figure(String bamboo_figure) {
        this.bamboo_figure = bamboo_figure;
    }

    public String getGrapheme() {
        return grapheme;
    }

    public void setGrapheme(String grapheme) {
        this.grapheme = grapheme;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

    public String getText_name() {
        return text_name;
    }

    public void setText_name(String text_name) {
        this.text_name = text_name;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }
}
